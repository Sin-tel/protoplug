local function Allpass()
	local a1, a2 = 0, 0
	local x1, x2 = 0, 0
	local y0, y1, y2 = 0, 0, 0

	local public = {
		update = function(w, r)
			a1 = -2 * r * math.cos(w)
			a2 = r * r
		end,
		process = function(x0)
			y2, y1 = y1, y0
			y0 = a2 * x0 + a1 * x1 + x2 - a1 * y1 - a2 * y2
			x2, x1 = x1, x0
			return y0
		end,
	}

	return public
end

